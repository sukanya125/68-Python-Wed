set1 = {1,2,3,4}
set2 = {3,4,5,6}

union_set = set1 | set2  # Union of two sets
print("Union", union_set)

intersection_set = set1 & set2  # Intersection of two sets
print("Intersection", intersection_set)

difference_set = set1 - set2  # Difference of two sets
print("Difference", difference_set)


sym_difference_set = set1 ^ set2  # Symmetric difference of two sets
print("Symmetric Difference", set1 ^ set2)  # Symmetric difference of two sets

